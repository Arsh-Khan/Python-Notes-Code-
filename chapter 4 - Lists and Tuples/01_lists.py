#create a lists using square bracket []
a = [1 , 2 , 4 , 56 , 6]

#printing lists using print function
print(a)

#access using index
print(a[0])

#change the value of list using
a[1] = 46
print(a)

#we can create lists with items of different types
c = [6 , "Arsh" , False , 6.9]
print(c)
print(type(c))

#list slicing
friends = ["Arsh" , "Harry", "Faaiz" , "Bill" , "Tom", 45]
print(friends[0:4])
print(friends[-4:])

